// Search foods by name
export function searchFoods(query: string): FoodItem[] {
    const lowerQuery = query.toLowerCase();
    return COMMON_FOODS.filter(food =>
        food.name.toLowerCase().includes(lowerQuery)
    );
}

// Get food by ID
export function getFoodById(id: string): FoodItem | undefined {
    return COMMON_FOODS.find(food => food.id === id);
}

// Get foods by category (based on macro composition)
export function getHighProteinFoods(): FoodItem[] {
    return COMMON_FOODS.filter(food => {
        const proteinCals = food.protein * 4;
        const totalCals = food.calories;
        return proteinCals / totalCals > 0.35;
    });
}

export function getLowCarbFoods(): FoodItem[] {
    return COMMON_FOODS.filter(food => {
        const carbCals = food.carbs * 4;
        const totalCals = food.calories;
        return carbCals / totalCals < 0.25;
    });
}
